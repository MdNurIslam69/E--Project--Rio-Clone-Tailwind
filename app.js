const openIcon = document.getElementsByClassName("open-icon");

const closeIcon = document.getElementsByClassName("close-icon");

const accordion = document.getElementsByClassName("accordion-content");

const accordionTittle = document.getElementsByClassName("accordion-tittle");

openIcon[0].addEventListener("click", () => {
  accordion[0].style.maxHeight = "500px";
  accordion[0].style.marginTop = "30px";
  accordion[0].style.opacity = "1";
  openIcon[0].style.display = "none";
  closeIcon[0].style.display = "block";
  accordionTittle[0].style.color = "blue";
  accordion[0].style.background = "white";
});

closeIcon[0].addEventListener("click", () => {
  accordion[0].style.maxHeight = "0";
  accordion[0].style.marginTop = "0";
  accordion[0].style.opacity = "0";
  openIcon[0].style.display = "block";
  closeIcon[0].style.display = "none";
  accordionTittle[0].style.color = "black";
});

